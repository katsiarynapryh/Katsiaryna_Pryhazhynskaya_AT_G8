package pretask;

public class MathOperatorsExample {
    static int a = 4;
    static int b = 5;
    public static void main(String[] args) {
        int plus = a + b;
        int minus = a - b;
        int multiplication = a * b;
        int division = a / b;
        System.out.println(plus);
        System.out.println(minus);
        System.out.println(multiplication);
        System.out.println(division);
    }
}
